#[cfg(test)]
mod tests {
    use super::*;
    use ox_core::ontology_ir::{
        EdgeTypeDef, EdgeTypeId, NodeTypeDef, NodeTypeId, PropertyDef, PropertyId,
    };

    fn test_ontology() -> OntologyIR {
        OntologyIR::new(
            "test-id".to_string(),
            "TestOntology".to_string(),
            Some("A test ontology".to_string()),
            1,
            vec![
                NodeTypeDef {
                    id: NodeTypeId::new("n1"),
                    label: "Customer".to_string(),
                    description: Some("A customer".to_string()),
                    source_table: None,
                    properties: vec![
                        PropertyDef {
                            id: PropertyId::new("p1"),
                            name: "id".to_string(),
                            property_type: PropertyType::String,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p2"),
                            name: "email".to_string(),
                            property_type: PropertyType::String,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p3"),
                            name: "phone".to_string(),
                            property_type: PropertyType::String,
                            nullable: true,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p4"),
                            name: "created_at".to_string(),
                            property_type: PropertyType::DateTime,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                    ],
                    constraints: vec![],
                },
                NodeTypeDef {
                    id: NodeTypeId::new("n2"),
                    label: "Product".to_string(),
                    description: None,
                    source_table: None,
                    properties: vec![
                        PropertyDef {
                            id: PropertyId::new("p5"),
                            name: "id".to_string(),
                            property_type: PropertyType::String,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p6"),
                            name: "price".to_string(),
                            property_type: PropertyType::Float,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p7"),
                            name: "tags".to_string(),
                            property_type: PropertyType::List {
                                element: Box::new(PropertyType::String),
                            },
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                    ],
                    constraints: vec![],
                },
            ],
            vec![EdgeTypeDef {
                id: EdgeTypeId::new("e1"),
                label: "PURCHASED".to_string(),
                description: Some("Customer bought a product".to_string()),
                source_node_id: NodeTypeId::new("n1"),
                target_node_id: NodeTypeId::new("n2"),
                properties: vec![PropertyDef {
                    id: PropertyId::new("ep1"),
                    name: "quantity".to_string(),
                    property_type: PropertyType::Int,
                    nullable: false,
                    default_value: None,
                    description: None,
                }],
                cardinality: ox_core::ontology_ir::Cardinality::ManyToMany,
            }],
            vec![],
        )
    }

    #[test]
    fn test_generate_python_node_dataclasses() {
        let ontology = test_ontology();
        let output = generate_python(&ontology);

        assert!(output.contains("@dataclass\nclass Customer:"));
        assert!(output.contains("    id: str"));
        assert!(output.contains("    email: str"));
        assert!(output.contains("    created_at: datetime"));
        assert!(output.contains("    phone: Optional[str] = None"));
    }

    #[test]
    fn test_generate_python_edge_dataclasses() {
        let ontology = test_ontology();
        let output = generate_python(&ontology);

        assert!(output.contains("class PurchasedEdge:"));
        assert!(output.contains("    \"\"\"Edge: Customer -> Product\"\"\""));
        assert!(output.contains("    source_id: str"));
        assert!(output.contains("    target_id: str"));
        assert!(output.contains("    quantity: int"));
    }

    #[test]
    fn test_generate_python_header_and_imports() {
        let ontology = test_ontology();
        let output = generate_python(&ontology);

        assert!(output.contains("# Auto-generated by Ontosyx"));
        assert!(output.contains("# Ontology: TestOntology v1"));
        assert!(output.contains("from __future__ import annotations"));
        assert!(output.contains("from dataclasses import dataclass, field"));
        assert!(output.contains("from datetime import datetime"));
        assert!(output.contains("from typing import Optional"));
    }

    #[test]
    fn test_required_before_optional_fields() {
        let ontology = test_ontology();
        let output = generate_python(&ontology);

        // In the Customer class, required fields (id, email, created_at)
        // should appear before optional fields (phone)
        let customer_section = output
            .split("class Customer:")
            .nth(1)
            .unwrap()
            .split("\n\n")
            .next()
            .unwrap();

        let id_pos = customer_section.find("    id: str").unwrap();
        let phone_pos = customer_section.find("    phone: Optional[str]").unwrap();
        assert!(
            id_pos < phone_pos,
            "Required field 'id' should appear before optional field 'phone'"
        );
    }
}
