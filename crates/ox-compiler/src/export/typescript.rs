#[cfg(test)]
mod tests {
    use super::*;
    use ox_core::ontology_ir::{
        EdgeTypeDef, EdgeTypeId, NodeTypeDef, NodeTypeId, PropertyDef, PropertyId,
    };

    fn test_ontology() -> OntologyIR {
        OntologyIR::new(
            "test-id".to_string(),
            "TestOntology".to_string(),
            Some("A test ontology".to_string()),
            1,
            vec![
                NodeTypeDef {
                    id: NodeTypeId::new("n1"),
                    label: "Customer".to_string(),
                    description: Some("A customer".to_string()),
                    source_table: None,
                    properties: vec![
                        PropertyDef {
                            id: PropertyId::new("p1"),
                            name: "id".to_string(),
                            property_type: PropertyType::String,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p2"),
                            name: "email".to_string(),
                            property_type: PropertyType::String,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p3"),
                            name: "phone".to_string(),
                            property_type: PropertyType::String,
                            nullable: true,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p4"),
                            name: "created_at".to_string(),
                            property_type: PropertyType::DateTime,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                    ],
                    constraints: vec![],
                },
                NodeTypeDef {
                    id: NodeTypeId::new("n2"),
                    label: "Product".to_string(),
                    description: None,
                    source_table: None,
                    properties: vec![
                        PropertyDef {
                            id: PropertyId::new("p5"),
                            name: "id".to_string(),
                            property_type: PropertyType::String,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p6"),
                            name: "price".to_string(),
                            property_type: PropertyType::Float,
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                        PropertyDef {
                            id: PropertyId::new("p7"),
                            name: "tags".to_string(),
                            property_type: PropertyType::List {
                                element: Box::new(PropertyType::String),
                            },
                            nullable: false,
                            default_value: None,
                            description: None,
                        },
                    ],
                    constraints: vec![],
                },
            ],
            vec![EdgeTypeDef {
                id: EdgeTypeId::new("e1"),
                label: "PURCHASED".to_string(),
                description: Some("Customer bought a product".to_string()),
                source_node_id: NodeTypeId::new("n1"),
                target_node_id: NodeTypeId::new("n2"),
                properties: vec![PropertyDef {
                    id: PropertyId::new("ep1"),
                    name: "quantity".to_string(),
                    property_type: PropertyType::Int,
                    nullable: false,
                    default_value: None,
                    description: None,
                }],
                cardinality: ox_core::ontology_ir::Cardinality::ManyToMany,
            }],
            vec![],
        )
    }

    #[test]
    fn test_generate_typescript_node_interfaces() {
        let ontology = test_ontology();
        let output = generate_typescript(&ontology);

        assert!(output.contains("export interface Customer {"));
        assert!(output.contains("  id: string;"));
        assert!(output.contains("  email: string;"));
        assert!(output.contains("  phone?: string;"));
        assert!(output.contains("  created_at: string;"));
        assert!(output.contains("export interface Product {"));
        assert!(output.contains("  price: number;"));
        assert!(output.contains("  tags: string[];"));
    }

    #[test]
    fn test_generate_typescript_edge_interfaces() {
        let ontology = test_ontology();
        let output = generate_typescript(&ontology);

        assert!(output.contains("/** Edge: Customer -[PURCHASED]-> Product */"));
        assert!(output.contains("export interface PurchasedEdge {"));
        assert!(output.contains("  source_id: string;"));
        assert!(output.contains("  target_id: string;"));
        assert!(output.contains("  quantity: number;"));
    }

    #[test]
    fn test_generate_typescript_header() {
        let ontology = test_ontology();
        let output = generate_typescript(&ontology);

        assert!(output.contains("// Auto-generated by Ontosyx"));
        assert!(output.contains("// Ontology: TestOntology v1"));
    }
}
